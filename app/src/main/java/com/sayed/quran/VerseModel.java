package com.sayed.quran;

public class VerseModel {
    String sura;
    String ayah;
    String arabic;
    String translation;
    String words;

    public VerseModel(String sura, String ayah, String arabic, String translation, String words) {
        this.sura = sura;
        this.ayah = ayah;
        this.arabic = arabic;
        this.translation = translation;
        this.words = words;
    }


}
